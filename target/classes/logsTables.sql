
DROP TABLE IF EXISTS CONTACTSBOOK;
CREATE TABLE CONTACTSBOOK
(
	id SERIAL PRIMARY KEY,
	Name TEXT NOT NULL,
	Mobile BIGINT NOT NULL,
	Office BIGINT,
	Stars INT,
	created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);



